//GPIO EXTI Interrupt Handler
//We have the following EXTI Lines
//Configured:
//PA0 - user button
//PE2 - falling with pullup - ok
//PE3 - falling with pullup - ok
//PE4 - falling with pullup - ok

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	//PA0 - Use this as the Missile Launch Button
	if (GPIO_Pin == userButton_Pin)
	{
		Sprite_SetMissileLaunchFlag();
	}

	//PE2 - Joystick Handler
	else if (GPIO_Pin == GPIO_PIN_2)
	{
		Sprite_SetMissileLaunchFlag();

		Joystick_ButtonHandler();
	}
	//PE3
	else if (GPIO_Pin == GPIO_PIN_3)
	{
		HAL_GPIO_TogglePin(ledRed_GPIO_Port, ledRed_Pin);
	}
	//PE4
	else if (GPIO_Pin == GPIO_PIN_4)
	{
		HAL_GPIO_TogglePin(ledRed_GPIO_Port, ledRed_Pin);
	}
}

